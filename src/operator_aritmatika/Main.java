package operator_aritmatika;

public class Main {

	public static void main(String[] args) {
		// operator aritmatika
		
		int a = 13;
		int b = 12;
		int hasil;
		
//		1. penjumlahan
		hasil = a + b;
		System.out.println(a + " + " + b + " = " + hasil);
		
//		2. pengurangan
		hasil = a - b;
		System.out.println(a + " - " + b + " = " + hasil);
		
//		3. perkalian
		hasil = a * b;
		System.out.println(a + " * " + b + " = " + hasil);
		
//		4. pembagian
		hasil = a / b;
		System.out.println(a + " / " + b + " = " + hasil);
		
//		5. modulasi / sisa bagi
		hasil = a % b;
		System.out.println(a + " % " + b + " = " + hasil);
	}

}
